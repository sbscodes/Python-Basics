a=5
b=7

print("Addition of {0} + {1}={2}".format(a,b,a+b))
#here arc bracket is treated as tuple
print("Addition of {0} - {1}={2}".format(a,b,a-b))
print("Addition of {0} / {1}={2}".format(a,b,a/b)) #Division
print("Addition of {0} * {1}={2}".format(a,b,a*b))
print("Addition of {0} // {1}={2}".format(a,b,a//b)) #Complete Division
print("Addition of {0} % {1}={2}".format(a,b,a%b)) #return remainder
print("Addition of {0} ** {1}={2}".format(a,b,a**b)) #here ** are like pow function